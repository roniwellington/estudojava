
public class VariavelASerTestada {
    public static void main(String[] args) {

		int mes = 1;

		switch (mes) {
			case 1:
				
				System.out.println("O mês é janeiro");
				break;
				
			case 2:
				System.out.println("O mes e fevereiro");
				break;
			
			case 3:
				System.out.println("O mes e marco");
				break;
				
			case 4:
				System.out.println("O mes e abril");
				break;
				
			case 5:
				System.out.println("O mes e maio");
				break;
			
			case 6:
				System.out.println("O mes junho");
				break;
			
			case 7:
				System.out.println("O mes de julho");
				break;
				
			case 8:
				System.out.println("O mes de agosto");
				break;
			
			case 9:
				System.out.println("O mes de setembro");
				break;
				
			case 10:
				System.out.println("O mes de outubro");
				break;
				
			case 11:
				System.out.println("O mes e novembro");
				break;
				
			case 12:
				System.out.println("O mes de dezembro");
				break;
				
			default:
				System.out.println("Mes invalido");
				break;
		}
	}
}
